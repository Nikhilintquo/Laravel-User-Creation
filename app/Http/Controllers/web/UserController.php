     */
    public function destroy($id)
    {
        //
        $user = User::where('id',$id)->first();
        $user->delete();

        $user = User::all();
        session(['alert' => 'User delete sucessfully','class'=>'alert-danger']);
        return redirect()->route('users')->with('user', $user);
